				<h4>Características</h4>
				<p>El FDRIVE, conectado a cualquier sistema de bombeo del mercado, garantiza su adecuado funcionamiento al mantener constante una determinada magnitud física (presión, presión diferencial, caudal, etc.) incluso al variar las condiciones de utilización. De tal modo la bomba o el sistema de bombeo, se accionan en medida a la demanda requerida, evitando así derroches de energía y alargándole la vida útil.</p>
			
